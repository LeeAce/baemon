        String filmname = "送初恋回家";
        String starname = "刘鑫 张雨提 高媛";
        int years = 2020;
        double grade = 9.0;
        System.out.println(filmname);
        System.out.println(starname);
        System.out.println(years);
        System.out.println(grade);
//      输出手机信息
        double price = 5299.99;
        String shoujiname  = "华为";
