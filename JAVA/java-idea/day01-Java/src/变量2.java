public class 变量2 {
    public static void main(String[] args) {
//输出打印
        int a = 10;
        System.out.println(a);
//参与计算
        int b = 20;
        int c = 30;
        System.out.println(b);
        System.out.println(c);
        System.out.println(b + c);
//修改记录的值
        a = 50;
        System.out.println(a);
//    一条语句中，可以定义多个变量
        int i=10,j = 20, k = 30;
        System.out.println(i);
        System.out.println(j);
        System.out.println(k);
//      变量在使用前一定要赋值
//      在定义时直接赋值
//      int g ;
//      g = 50;
        int g=50;
        System.out.println(g);


    }

}
